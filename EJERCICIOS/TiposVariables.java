public class TiposVariables{
	public static void main(String[] args){
		//Tipos de variables primitivos
		char miChar = 'l';
		System.out.println("La variable miChar vale: "+miChar);
		char miHexaChar = 0x6A;
		//también, se puede poner su código ASCCI
		char otroChar = 70;
		System.out.println("La variable otroChar vale: "+otroChar);
		System.out.println("La variable miHexaChar: "+miHexaChar);
	}
}
//Para hexadecimal se pone 0x delante del numero.
